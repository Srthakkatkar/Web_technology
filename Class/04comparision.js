// Topic: Comparison Operators

// Basic number comparisons

console.log(2 > 3);      

console.log(2 < 3);      

console.log(2 >= 2);     

console.log(2 <= 1);     

console.log(2 == 2);     

console.log(2 != 3);     

console.log("2" > 1);    

console.log("02" > 1);   


// null comparisons
console.log(null > 0);   

console.log(null == 0);  

console.log(null <= 0);  



// undefined comparisons
console.log(undefined > 0);  

console.log(undefined < 0);  

console.log(undefined == 0); 



// Strict comparison
console.log("2" === 2);  


